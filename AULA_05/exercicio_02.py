# Laço de Repetição:

resp = "S"  # Controla a repetição do laço
while resp == "S": # Continua pedindo nomes enquanto o usuário digitar "S"
    nome = input("Informe o nome do usuário:") # Solicita o nome do usuário
    resp = input("Deseja Continuar(S/N)?")  # Pergunta se deseja continuar
print(nome) # Exibe o último nome informado



