# Análise de Idades:

idades = [20,10,15,30,50,12,60,22,55,35]
print(f" A Soma das idades é: {sum(idades)} anos.")  # sun = soma
print(f" A Maior idade é: {max(idades)} anos.")  # max = maior
print(f" A Menor idade é: {min(idades)} anos.")  # min = menor
print(f" A Média das idades é: {sum(idades)/len(idades)} anos.") # len = tamanho
idades.sort()
print(idades)

